     *
     * @var array
     */
    protected $casts = [
        'id' => 'integer',
        'url' => 'string',
        'fk_entidad' => 'integer'
    ];

    /**
     * Validation rules
     *
     * @var array
     */
    public static $rules = [
        
    ];

